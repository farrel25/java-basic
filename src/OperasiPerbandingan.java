public class OperasiPerbandingan {

    public static void main(String[] args) {
        byte a = 100;
        byte b = 100;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
